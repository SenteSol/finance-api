import mongoose from 'mongoose';
const Schema = mongoose.Schema;

//Create Schema
const UserSchema = new Schema({
    userId: {
        type: String,
        required: true,
    },
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
});

let User = mongoose.model('users', UserSchema);

export default User;
